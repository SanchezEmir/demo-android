package com.sanchezemir.appcobranza.models.clientes;

public class Cliente {

    private Integer id;
    private String dni;
    private String telefono;
    private String name;

    public Integer getId() {
        return id;
    }

    public String getDni() {
        return dni;
    }

    public String getTelefono() {
        return telefono;
    }

    public String getName() {
        return name;
    }
}